age=int(input("How old are you?"))
print(age<14)
if age<14:
    print("You are under fourteen")
if age>14:
    print("You are older fourteen")
if age==14:
    print("You are FOURTEEN")
if age>=14:
    print("You are older fourteen or you are fourteen")
if age<=14:
    print("You are under fourteen or you are fourteen")
if age!=14:
    print("You are not fourteen")
